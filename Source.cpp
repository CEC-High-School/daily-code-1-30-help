#include <iostream>
using namespace std;
int main() {
	
	for (int i = 20; i <= 80; i++)
		cout << i<<" ";

	cout << endl << endl;

	for (int r = 70; r >= 10; r--)
		cout << r<<" ";

	cout << endl;

	int j = 20;
	while (j <= 80) {
		cout << j << endl;
		j++;
	}

	cout << endl;

	int e = 70;
	while (e >= 10) {
		cout << e << endl;
		e--;
	}
	





}


